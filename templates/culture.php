Tous les deux ans, en alternance avec Rio de Janeiro, Lisbonne accueille le plus grand festival du monde Rock in Rio. L’événement se tient au Parque da Bela Vista, avec la présence de 70 artistes sur quatre scènes au cours des cinq jours du festival, qui réunissent 385 000 spectateurs. Lors de l'édition 2014 célébrant les 10 ans du festival, de grands artistes se sont produits sur la grande scène lisboète tels que The Rolling Stones, Justin Timberlake, Robbie Williams, Linkin Park, Ed Sheeran, Jessie J, Lorde.</p>




